    private void formMouseEntered(java.awt.event.MouseEvent evt) {
    mantenerHover();
}

    private void formMouseExited(java.awt.event.MouseEvent evt) {
        verificarSalidaDelMouse();
    }

    private void editarContactoButtonMouseEntered(java.awt.event.MouseEvent evt) {
        editarContactoButton.setBorder(BorderFactory.createLineBorder(Constantes.COLOR_NOTIFICACION, 1));
        mantenerHover();
    }

    private void editarContactoButtonMouseExited(java.awt.event.MouseEvent evt) {
        editarContactoButton.setBorder(BorderFactory.createEmptyBorder());
        verificarSalidaDelMouse();
    }

    private void editarContactoButtonActionPerformed(java.awt.event.ActionEvent evt) {
        ActionEvent event = new ActionEvent(nicknameRealLabel.getText(), ActionEvent.ACTION_PERFORMED, "SOLICITUD EDITAR CONTACTO");
        controlador.actionPerformed(event);
    }

    private void mantenerHover() {
        panelNickname.setBackground(Constantes.COLOR_HOVER);
        nicknameAgendadoLabel.setForeground(new Color(30, 30, 30));
        nicknameRealLabel.setForeground(new Color(30, 30, 30));
        editarContactoButton.setVisible(true);
    }

    private void salirHover() {
        panelNickname.setBackground(Constantes.COLOR_BASE);
        nicknameAgendadoLabel.setForeground(Color.WHITE);
        nicknameRealLabel.setForeground(Color.WHITE);
        editarContactoButton.setVisible(false);
    }

    private void verificarSalidaDelMouse() {
        // Obtenemos posici√≥n del mouse en pantalla
        Point mouseEnPantalla = MouseInfo.getPointerInfo().getLocation();
        // Convertimos a coordenadas relativas al componente
        SwingUtilities.convertPointFromScreen(mouseEnPantalla, this);
        if (!this.contains(mouseEnPantalla)) {
            salirHover();
        }
}

